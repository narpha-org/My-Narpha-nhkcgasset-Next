export const getServerSideProps: GetServerSideProps = async () => {

  let data: Sample[] = []

  await apolloServer
    .query({
      query: SamplesAllDocument,
      fetchPolicy: "network-only",
    })
    .then((res: ApolloQueryResult<{
      samplesAll: Sample[];
    }>) => {

      data = res.data.samplesAll

    }).catch(err => {
      console.error(`[index][getServerSideProps]`)
      console.error(err)
    })

  return {
    props: {
      data
    }
  }
}

